// Template Literals

/**
 * Template literals make strings easier to write.
 * They use backticks: ` `
 */


// Adding variables inside a string

const studentName = "Asha";

const message = `Hello, ${studentName}`;

console.log(message); // Hello, Asha


// Using expressions inside a string

const a = 5;
const b = 10;

console.log(`The sum is ${a + b}`); // The sum is 15


// Multi-line string

const note = `This is line one.
This is line two.`;

console.log(note);
