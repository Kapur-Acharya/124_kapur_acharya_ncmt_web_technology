// Variables in ES6

/**
 * Variables store values.
 * ES6 uses let and const.
 */


// let
/**
 * Use let when the value can change.
 */

let name = "Asha";
name = "Ram";

console.log(name); // Ram


// const
/**
 * Use const when the value should not be changed.
 */

const age = 18;

console.log(age); // 18


// let vs const
/**
 * let   = value can change
 * const = value cannot be reassigned
 */
